areaRectangle :: Integer -> Integer -> Integer
areaRectangle width height = width * height

perimeterRectangle :: Integer -> Integer -> Integer
perimeterRectangle width height = 2 * (width + height)

areaCircle :: Float -> Float
areaCircle radius = pi * radius * radius

perimeterCircle :: Float -> Float
perimeterCircle radius = 2 * pi * radius